use anyhow::Result;

use crate::{
    aead::gcm,
    block_cipher::aes::{Aes, Aes128Cipher, Aes256Cipher},
};

#[inline]
pub fn encrypt_aes_128_gcm(
    secret: &[u8],
    iv: &[u8],
    plaintext: &[u8],
    additional_data: &[u8],
) -> Result<(Box<[u8]>, Box<[u8]>)> {
    let block_cipher = Aes::new(Aes128Cipher::new(secret.try_into()?));
    gcm::encrypt(&block_cipher, iv, plaintext, additional_data)
}

#[inline]
pub fn encrypt_aes_256_gcm(
    secret: &[u8],
    iv: &[u8],
    plaintext: &[u8],
    additional_data: &[u8],
) -> Result<(Box<[u8]>, Box<[u8]>)> {
    let block_cipher = Aes::new(Aes256Cipher::new(secret.try_into()?));
    gcm::encrypt(&block_cipher, iv, plaintext, additional_data)
}

#[inline]
pub fn decrypt_aes_256_gcm(
    secret: &[u8],
    iv: &[u8],
    ciphertext: &[u8],
    additional_data: &[u8],
    tag: &[u8],
) -> Result<Box<[u8]>> {
    let block_cipher = Aes::new(Aes256Cipher::new(secret.try_into()?));
    gcm::decrypt(&block_cipher, iv, ciphertext, additional_data, tag)
}

