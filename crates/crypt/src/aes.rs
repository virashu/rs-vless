// 0   4   8  12
// 1   5   9  13
// 2   6  10  14
// 3   7  11  15
#[rustfmt::skip]
fn shift_rows(state: [u8; 16]) -> [u8; 16] {
    [
        state[0],  state[5],  state[10], state[15],
        state[4],  state[9],  state[14], state[3],
        state[8],  state[13], state[2],  state[7],
        state[12], state[1],  state[6],  state[11],
    ]
}

#[rustfmt::skip]
fn shift_rows_inv(state: [u8; 16]) -> [u8; 16] {
    [
        state[0],  state[13], state[10], state[7],
        state[4],  state[1],  state[14], state[11],
        state[8],  state[5],  state[2],  state[15],
        state[12], state[9],  state[6],  state[3],
    ]
}

fn shift_column(col: [u8; 4]) -> [u8; 4] {
    [col[1], col[2], col[3], col[0]]
}

fn sub_byte(value: u8) -> u8 {
    AES_S_BOX[value as usize]
}

fn sub_byte_inv(value: u8) -> u8 {
    AES_INVERSE_S_BOX[value as usize]
}

fn sub_bytes(value: [u8; 16]) -> [u8; 16] {
    value.map(sub_byte)
}

fn sub_bytes_inv(value: [u8; 16]) -> [u8; 16] {
    value.map(sub_byte_inv)
}

fn sub_bytes_col(col: [u8; 4]) -> [u8; 4] {
    col.map(sub_byte)
}

/// ISO/IEC 18033-3:2010 - 5.2.4.6
fn mix_column(col: [u8; 4]) -> [u8; 4] {
    let clamp = |x: u16| if x > 0xff { (x ^ 0x1b) & 0xFF } else { x };

    let s0 = col[0] as u16;
    let s1 = col[1] as u16;
    let s2 = col[2] as u16;
    let s3 = col[3] as u16;

    let s0r = clamp(s0 * 2) ^ (clamp(s1 * 2) ^ s1) ^ s2 ^ s3;
    let s1r = s0 ^ clamp(s1 * 2) ^ (clamp(s2 * 2) ^ s2) ^ s3;
    let s2r = s0 ^ s1 ^ clamp(2 * s2) ^ (clamp(s3 * 2) ^ s3);
    let s3r = (clamp(s0 * 2) ^ s0) ^ s1 ^ s2 ^ clamp(2 * s3);

    [s0r as u8, s1r as u8, s2r as u8, s3r as u8]

    // let mut b = [0; 4];

    // for c in 0..4 {
    //     b[c] = (col[c] << 1) ^ ((col[c] >> 7) * 0x1B);
    // }

    // [
    //     b[0] ^ col[3] ^ col[2] ^ (b[1] ^ col[1]),
    //     b[1] ^ col[0] ^ col[3] ^ (b[2] ^ col[2]),
    //     b[2] ^ col[1] ^ col[0] ^ (b[3] ^ col[3]),
    //     b[3] ^ col[2] ^ col[1] ^ (b[0] ^ col[0]),
    // ]
}

/// ISO/IEC 18033-3:2010 - 5.2.4.6
fn mix_columns(state: [u8; 16]) -> [u8; 16] {
    state
        .chunks_exact(4)
        .flat_map(|col| mix_column(col.try_into().unwrap()))
        .collect::<Vec<_>>()
        .try_into()
        .unwrap()
}

/// ISO/IEC 18033-3:2010 - 5.2.4.7
fn mix_column_inv(col: [u8; 4]) -> [u8; 4] {
    let s0 = col[0];
    let s1 = col[1];
    let s2 = col[2];
    let s3 = col[3];

    let s0r = (0x0e * s0) ^ (0x0b * s1) ^ (0x0d * s2) ^ (0x09 * s3);
    let s1r = (0x09 * s0) ^ (0x0e * s1) ^ (0x0b * s2) ^ (0x0d * s3);
    let s2r = (0x0d * s0) ^ (0x09 * s1) ^ (0x0e * s2) ^ (0x0b * s3);
    let s3r = (0x0b * s0) ^ (0x0d * s1) ^ (0x09 * s2) ^ (0x0e * s3);

    [s0r, s1r, s2r, s3r]
}

/// ISO/IEC 18033-3:2010 - 5.2.4.7
fn mix_columns_inv(state: [u8; 16]) -> [u8; 16] {
    state
        .chunks_exact(4)
        .flat_map(|col| mix_column_inv(col.try_into().unwrap()))
        .collect::<Vec<_>>()
        .try_into()
        .unwrap()
}

fn add_round_key(state: [u8; 16], words: [u32; 4]) -> [u8; 16] {
    state
        .chunks_exact(4)
        .zip(words.iter())
        .flat_map(|(col, word)| (u32::from_be_bytes(col.try_into().unwrap()) ^ word).to_be_bytes())
        .collect::<Vec<_>>()
        .try_into()
        .unwrap()
}

#[rustfmt::skip]
const RC: &[u32] = &[
    0x0100_0000,
    0x0200_0000,
    0x0400_0000,
    0x0800_0000,
    0x1000_0000,
    0x2000_0000,
    0x4000_0000,
    0x8000_0000,
    0x1b00_0000,
    0x3600_0000,
];

// fn rc(i: u32) -> [u8; 4] {
//     [0x02u8.pow(i - 1), 0, 0, 0]
// }

const AES_BLOCK_SIZE: usize = 16;

pub struct Aes<C>
where
    C: AesCipher,
{
    cipher: C,
}

impl<C> Aes<C>
where
    C: AesCipher,
{
    pub fn new(cipher: C) -> Self {
        Self { cipher }
    }

    fn encrypt_block(&self, block: [u8; AES_BLOCK_SIZE]) -> [u8; AES_BLOCK_SIZE] {
        let w_0 = self.cipher.get_round_key(0);
        let mut state = add_round_key(block, w_0);

        for i in 1..C::ROUNDS {
            let w_i = self.cipher.get_round_key(i);

            state = sub_bytes(state);
            state = shift_rows(state);
            state = mix_columns(state);
            state = add_round_key(state, w_i);
        }

        let w_n = self.cipher.get_round_key(C::ROUNDS);
        state = sub_bytes(state);
        state = shift_rows(state);
        state = add_round_key(state, w_n);

        state
    }

    pub fn encrypt(&self, plaintext: &[u8]) -> Box<[u8]> {
        let mut plaintext = Vec::from(plaintext);
        let unpadded = plaintext.len() % AES_BLOCK_SIZE;

        if unpadded != 0 {
            plaintext.extend(vec![0; AES_BLOCK_SIZE - unpadded]);
        }

        plaintext
            .chunks_exact(AES_BLOCK_SIZE)
            .flat_map(|chunk| {
                #[allow(clippy::missing_panics_doc, reason = "exact chunks")]
                let chunk: [u8; AES_BLOCK_SIZE] = chunk.try_into().unwrap();
                self.encrypt_block(chunk)
            })
            .collect::<Box<[u8]>>()
    }
}

pub trait AesCipher {
    const ROUNDS: usize;

    fn get_round_key(&self, i: usize) -> [u32; 4];
}

/// 4 * (ROUNDS + 1)
const AES_128_WORDS: usize = 44;

pub struct Aes128Cipher {
    words: Box<[u32; AES_128_WORDS]>,
}

impl Aes128Cipher {
    pub fn new(secret_key: &[u8; 16]) -> Self {
        let words = Self::expand_key(*secret_key);

        Self {
            words: Box::new(words),
        }
    }

    fn expand_key(key: [u8; 16]) -> [u32; AES_128_WORDS] {
        const NK: usize = 4;

        let mut w = [0; AES_128_WORDS];

        for i in 0..NK {
            w[i] = u32::from_be_bytes(key[(i * 4)..(i * 4 + 4)].try_into().unwrap());
        }

        for j in NK..AES_128_WORDS {
            if j % NK == 0 {
                w[j] = w[j - NK]
                    ^ u32::from_be_bytes(sub_bytes_col(shift_column(w[j - 1].to_be_bytes())))
                    ^ RC[(j - 4) / NK];
            } else {
                w[j] = w[j - NK] ^ w[j - 1];
            }
        }

        w
    }
}

impl AesCipher for Aes128Cipher {
    const ROUNDS: usize = 10;

    fn get_round_key(&self, i: usize) -> [u32; 4] {
        [
            self.words[4 * i],
            self.words[4 * i + 1],
            self.words[4 * i + 2],
            self.words[4 * i + 3],
        ]
    }
}

impl Drop for Aes128Cipher {
    fn drop(&mut self) {
        self.words.fill(0);
    }
}

/// 4 * (ROUNDS + 1)
const AES_256_WORDS: usize = 60;

pub struct Aes256Cipher {
    words: Box<[u32; AES_256_WORDS]>,
}

impl Aes256Cipher {
    pub fn new(secret_key: &[u8; 32]) -> Self {
        let words = Self::expand_key(*secret_key);

        Self {
            words: Box::new(words),
        }
    }

    fn expand_key(key: [u8; 32]) -> [u32; AES_256_WORDS] {
        const NK: usize = 8;

        let mut w = [0; AES_256_WORDS];

        for i in 0..NK {
            w[i] = u32::from_be_bytes(key[(i * 4)..(i * 4 + 4)].try_into().unwrap());
        }

        for j in NK..AES_256_WORDS {
            if j % NK == 0 {
                w[j] = w[j - NK]
                    ^ u32::from_be_bytes(sub_bytes_col(shift_column(w[j - 1].to_be_bytes())))
                    ^ RC[(j - 4) / NK];
            } else if j % NK == 4 {
                w[j] = w[j - NK] ^ u32::from_be_bytes(sub_bytes_col(w[j - 1].to_be_bytes()));
            } else {
                w[j] = w[j - NK] ^ w[j - 1];
            }
        }

        w
    }
}

