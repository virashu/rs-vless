use crate::hash::Hasher;

pub fn hmac_hash<H: Hasher>(key: &[u8], text: &[u8]) -> Box<[u8]> {
    let text_1 = {
        let mut x = vec![0x36; H::BLOCK_SIZE];
        key.iter().enumerate().for_each(|(i, k)| x[i] ^= k);
        x.extend(text);
        x
    };
    let hashsum_1 = H::hash(&text_1);

    let text_2 = {
        let mut x = vec![0x5c; H::BLOCK_SIZE];
        key.iter().enumerate().for_each(|(i, k)| x[i] ^= k);
        x.extend(hashsum_1);
        x
    };
    H::hash(&text_2)
}

/// <https://datatracker.ietf.org/doc/html/rfc4231>
#[cfg(test)]
mod tests {
    use crate::hash::sha::Sha256;

    use super::*;

    #[test]
    fn test_hmac_sha256_1() {
        let key = [
            0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
            0x0b, 0x0b, 0x0b, 0x0b, 0x0b, 0x0b,
        ];
        let data = b"Hi There";

        assert_eq!(
            *hmac_hash::<Sha256>(&key, data),
            [
                0xb0, 0x34, 0x4c, 0x61, 0xd8, 0xdb, 0x38, 0x53, 0x5c, 0xa8, 0xaf, 0xce, 0xaf, 0x0b,
                0xf1, 0x2b, 0x88, 0x1d, 0xc2, 0x00, 0xc9, 0x83, 0x3d, 0xa7, 0x26, 0xe9, 0x37, 0x6c,
                0x2e, 0x32, 0xcf, 0xf7,
            ]
        );
    }

    #[test]
    fn test_hmac_sha256_2() {
        let key = [0x4a, 0x65, 0x66, 0x65];
        let data = b"what do ya want for nothing?";

        assert_eq!(
            *hmac_hash::<Sha256>(&key, data),
            [
                0x5b, 0xdc, 0xc1, 0x46, 0xbf, 0x60, 0x75, 0x4e, 0x6a, 0x04, 0x24, 0x26, 0x08, 0x95,
                0x75, 0xc7, 0x5a, 0x00, 0x3f, 0x08, 0x9d, 0x27, 0x39, 0x83, 0x9d, 0xec, 0x58, 0xb9,
                0x64, 0xec, 0x38, 0x43,
            ]
        );
    }
}
